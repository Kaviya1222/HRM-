def _permission(
    *,
    key: str,
    name: str,
    module: str,
    category: PermissionCategory,
    resource: str,
    action: str,
    description: str,
) -> dict[str, str]:
    return {
        "key": key,
        "name": name,
        "module": module,
        "category": category.value,
        "resource": resource,
        "action": action,
        "description": description,
    }


