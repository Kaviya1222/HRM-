def role_hierarchy_seed() -> list[dict[str, int | str]]:
    items: list[dict[str, int | str]] = []
    for index, code in enumerate(ROLE_HIERARCHY, start=1):
        items.append({"code": code.value, "name": ROLE_DISPLAY_NAMES[code], "hierarchy_rank": index})
    return items
